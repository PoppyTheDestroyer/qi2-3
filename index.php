    <script>
        function scrollToDiv(el) {
            $('html, body').animate({
                scrollTop: $(el).offset().top
            }, 1000);
        }
        var speed = 3000;
        var slides = $('.slide');
        var container = $('#slides ul');
        var elm = container.find(':first-child').prop("tagName");
        var item_width = container.width();
        var previous = 'prev'; //id of previous button
        var next = 'next'; //id of next button
        $(document).ready(function () {
            //rotation speed and timer
            var run = setInterval(rotate, speed);

            slides.width(item_width); //set the slides to the correct pixel width
            container.parent().width(item_width);
            container.width(slides.length * item_width); //set the slides container to the correct total width
            container.find(elm + ':first').before(container.find(elm + ':last'));
            resetSlides();


            //if user clicked on prev button



            //if mouse hover, pause the auto rotation, otherwise rotate it
            container.parent().mouseenter(function () {
                clearInterval(run);
            }).mouseleave(function () {
                run = setInterval(rotate, speed);
            });

            $(window).resize(function () {

                var mh = 0;
                $('.carousel-item h2.mob_center').each(function () {
                    if ($(this).height() > mh)
                        mh = $(this).height();
                });
                var mt = 100 + mh;
                if ($(this).width() < 576) {
                    $('.carousel-item').height($('.homesignup').height() + mt + 100).css({ 'padding-top': $('.quick_links').height() + 'px' });
                    $('.homesignup').css({ 'margin-top': mt + 'px' });
                }


            }).resize();

        });
        //a simple function to click next link
        //a timer will call this function, and the rotation will begin
        function resetSlides() {
            //and adjust the container so current is in the frame
            container.css({
                'left': -1 * item_width
            });
        }
        function rotate() {
            moveSlide(next);
        }
        function moveSlide(dir) {
            //slide the item

            if (container.is(':animated')) {
                return false;
            }
            if (dir == previous) {
                container.stop().animate({
                    'left': 0
                }, 500, function () {
                    container.find(elm + ':first').before(container.find(elm + ':last'));
                    resetSlides();
                });
            }

            if (dir == next) {
                container.stop().animate({
                    'left': item_width * -2
                }, 500, function () {
                    container.find(elm + ':last').after(container.find(elm + ':first'));
                    resetSlides();
                });
            }

            //cancel the link behavior
            return false;

        };

        window.onscroll = function () { myFunction() };

        // Get the header
        var footer = document.getElementById("myFooter");

        // Get the offset position of the navbar
        var sticky = footer.offsetTop;

        // Add the sticky class to the header when you reach its scroll position. Remove "sticky" when you leave the scroll position
        function myFunction() {
            if (window.pageYOffset >= sticky) {
                footer.classList.remove("footer");
                footer.classList.remove("foot");
                footer.classList.add("sticky");
            } else {
                footer.classList.add("footer");
                footer.classList.remove("sticky");
            }
        }
    </script>
